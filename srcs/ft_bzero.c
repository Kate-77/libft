#include "../libft.h"
void	ft_bzero(void *str, size_t n)
{
	unsigned char	*str0;

	str0 = (unsigned char *)str;
	while (n)
	{
		*str0 = '\0';
		str0++;
		n--;
	}
	return ;
}
